package com.example.hsxfd.cyzim.common.activity;

import android.content.Context;
import android.os.Build;
import android.os.Bundle;
import android.os.Handler;
import android.os.PersistableBundle;
import android.support.annotation.Nullable;
import android.support.v4.app.FragmentManager;
import android.support.v4.app.FragmentTransaction;
import android.support.v7.app.AppCompatActivity;
import android.support.v7.widget.Toolbar;
import android.text.TextUtils;
import android.view.MenuItem;
import android.view.View;
import android.view.inputmethod.InputMethodManager;

import com.example.hsxfd.cyzim.common.fragment.TFragment;
import com.example.hsxfd.cyzim.common.util.log.LogUtil;
import com.example.hsxfd.cyzim.common.util.sys.ReflectionUtil;

import java.util.ArrayList;
import java.util.List;


